package variablesStudy;

public class CharStudy {

	public static void main(String[] args) {

		//1.Variable Declaration
		char grade;
		char gender;
		
		//2.Variable Initialization
		grade = 'A';
		gender = 'F';
		
		//3.Usage
		System.out.println("Grade is: "+grade);
		System.out.println("Gender is: "+gender);
		
	}

}
