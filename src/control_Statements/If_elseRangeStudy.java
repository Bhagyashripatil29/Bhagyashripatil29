package control_Statements;

public class If_elseRangeStudy {

	public static void main(String[] args) {

		//if marks 66-99=A then you got grade A
		
		int marks=90;
		
		if(marks>=66 & marks<=99) {
			
			System.out.println("You got grade A");
		}
		else
			System.out.println("You got other than grade A");
	}

}
